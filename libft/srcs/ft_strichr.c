#include "../includes/libft.h"

int		ft_strichr(const char *s, int c)
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	if (!s)
		return (0);
	while (s[++i])
	{
		j++;
		if (s[i] == (char)c)
			break ;
	}
	return (i);
}
